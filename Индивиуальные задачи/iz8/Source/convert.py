def name_to_key(name):
    for key in monster_name:
        if name == monster_name[key]:
            return key
    key = translit_name(name)
    return key

def translit_name(name):
    name = name.lower(); i = 0
    while i < len(name):
        if name[i] in transl_dict:
            name = name.replace(name[i], transl_dict[name[i]])
        i += 1
    return name


