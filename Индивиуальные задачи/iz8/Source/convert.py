#-------Ключ в имя чудовища--------
#----------------------------------


def key_to_name(key):
    if key in monster_name:
        return monster_name[key]
    else:
        key = translit_key(key)
        return key

def translit_key(key):
    for part in transl_len:
        for elem in part:
            key = key.replace(transl_dict[elem], elem)
    key = key.replace('_', ' ')
    key = key.capitalize()
    return key

